public class HelloUniverse {

    public static void main(String... args){
        String text = "Aux dernières nouvelles, le nombre de planètes dans le système solaire est de :";
        int numberOfPlanets = 8;
        System.out.println(text);
        System.out.println(numberOfPlanets);

        text = "Il y a quelques années cependant, elles étaient au nombre de :";
        numberOfPlanets = 9;
        System.out.println(text);
        System.out.println(numberOfPlanets);
    }

}